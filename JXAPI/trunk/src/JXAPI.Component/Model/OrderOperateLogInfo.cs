using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace JXAPI.Component
{
    public class OrderOperateLogInfo
    {
        /// <summary>
        /// 记录编号
        /// </summary>
        public long LogID { get; set; }

        /// <summary>
        /// 订单编号
        /// </summary>
        public string OrderID { get; set; }

        /// <summary>
        /// 三方单号
        /// </summary>
        public int ThirdOrderID { get; set; }

        /// <summary>
        /// 拆单单号
        /// </summary>
        public string OrderSplitID { get; set; }

        /// <summary>
        /// 用户登录编号
        /// </summary>
        public int UID { get; set; }

        /// <summary>
        /// 用户类型 0：买家 2：客服  3：结算  4：物流 5：财务 6：管理员
        /// </summary>
        public short UserType { get; set; }

        /// <summary>
        /// 用户名
        /// </summary>
        public string UserName { get; set; }

        /// <summary>
        /// 标题（一般是默认值）
        /// </summary>
        public string Tittle { get; set; }

        /// <summary>
        /// 内容
        /// </summary>
        public string Content { get; set; }

        /// <summary>
        /// 老订单状态
        /// </summary>
        public short OldStatus { get; set; }

        /// <summary>
        /// 新订单状态
        /// </summary>
        public short NewStatus { get; set; }

        /// <summary>
        /// 创建时间
        /// </summary>
        public DateTime CreateTime { get; set; }

        /// <summary>
        /// 评论状态 0：正常；1：不显示
        /// </summary>
        public short Status { get; set; }
    }
}
