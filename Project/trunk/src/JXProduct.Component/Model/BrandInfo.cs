using JXProduct.Component.Enums;
using System;

namespace JXProduct.Component.Model
{
    [Serializable]
    public class BrandInfo
    {
        public BrandInfo() { }
        /// <summary>
        /// 品牌ID
        /// </summary>
        public int BrandID { get; set; }

        /// <summary>
        /// 品牌中文名称
        /// </summary>
        public string ChineseName { get; set; }

        public string PinyinName { get; set; }
        /// <summary>
        /// 品牌英文名称
        /// </summary>
        public string EnglishName { get; set; }

        /// <summary>
        /// 注册商标（P库）
        /// </summary>
        public string RegTrademark { get; set; }

        /// <summary>
        /// 非注册商标
        /// </summary>
        public string UnregTrademark { get; set; }

        /// <summary>
        /// 品牌分类(0：中华老字号 1：国际著名品牌 2：知名品牌 3：普通品牌)
        /// </summary>
        public short BrandType { get; set; }

        /// <summary>
        /// Logo图
        /// </summary>
        public string ImageLogo { get; set; }

        /// <summary>
        /// Banner图
        /// </summary>
        public string ImageBanner { get; set; }

        /// <summary>
        /// 品牌等级
        /// </summary>
        public short Level { get; set; }

        /// <summary>
        /// 品牌路径
        /// </summary>
        public string Path { get; set; }

        /// <summary>
        /// 父品牌号
        /// </summary>
        public int ParentID { get; set; }

        /// <summary>
        /// 品牌级数
        /// </summary>
        public short PathCount { get; set; }

        /// <summary>
        /// 品牌标题
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// 关键词
        /// </summary>
        public string Keywords { get; set; }

        /// <summary>
        /// 品牌描述
        /// </summary>
        public string Description { get; set; }

        /// <summary>
        /// 
        /// </summary>
        public string MetaDescription { get; set; }

        /// <summary>
        /// 排序
        /// </summary>
        public int Sort { get; set; }

        /// <summary>
        /// 状态 0：可用 1：不可用
        /// </summary>
        public short Status { get; set; }

        /// <summary>
        /// 
        /// </summary>
        public int SellCount { get; set; }

        /// <summary>
        /// 
        /// </summary>
        public int ProductCount { get; set; }

    }
}

